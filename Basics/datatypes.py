# Numeric Types
integer_number = 42
float_number = 3.14
complex_number = 2 + 3j

# Sequence Types
string = "Hello, World!"
list_data = [1, 2, 3, "apple", "banana"]
tuple_data = (4, 5, 6, "cherry", "orange")

# Mapping Type
dictionary = {"name": "John", "age": 30, "city": "New York"}

# Set Types
set_data = {1, 2, 3, 4, 5}
frozen_set = frozenset({6, 7, 8})

# Boolean Type
boolean = True

# None Type
none_value = None

# Binary Types
bytes_data = b"bytes"
bytearray_data = bytearray(b"bytearray")

# Memory Views
memory_view = memoryview(bytes_data)

# Displaying values
print("Integer Number:", integer_number)
print("Float Number:", float_number)
print("Complex Number:", complex_number)
print("String:", string)
print("List:", list_data)
print("Tuple:", tuple_data)
print("Dictionary:", dictionary)
print("Set:", set_data)
print("Frozen Set:", frozen_set)
print("Boolean:", boolean)
print("None Value:", none_value)
print("Bytes:", bytes_data)
print("Bytearray:", bytearray_data)
print("Memory View:", memory_view)
